WITH PRODUCT AS (
    SELECT 
        PRODUCTID, 
        CAST(NAME AS VARCHAR(50)) AS NAME, 
        CAST(PRODUCTNUMBER AS VARCHAR(12)) AS PRODUCTNUMBER, 
        MAKEFLAG, 
        FINISHEDGOODSFLAG, 
        CAST(COLOR AS VARCHAR(30)) AS COLOR, 
        SAFETYSTOCKLEVEL, 
        REORDERPOINT, 
        STANDARDCOST, 
        LISTPRICE, 
        CAST(SIZE AS VARCHAR(4)) AS SIZE, 
        CAST(SIZEUNITMEASURECODE AS VARCHAR(5)) AS SIZEUNITMEASURECODE, 
        CAST(WEIGHTUNITMEASURECODE AS VARCHAR(5)) AS WEIGHTUNITMEASURECODE, 
        WEIGHT, 
        DAYSTOMANUFACTURE, 
        CAST(PRODUCTLINE AS VARCHAR(4)) AS PRODUCTLINE, 
        CAST(CLASS AS VARCHAR(4)) AS CLASS, 
        CAST(STYLE AS VARCHAR(4)) AS STYLE, 
        PRODUCTSUBCATEGORYID, 
        PRODUCTMODELID, 
        CAST(SELLSTARTDATE AS DATE) AS SELLSTARTDATE, 
        CAST(SELLENDDATE AS DATE) AS SELLENDDATE, 
        --CAST(DISCONTINUEDDATE AS DATE) AS DISCONTINUEDDATE, --CAMPO DESCARTADO, SOMENTE CONTÃ‰M VALORES NULOS 
        CAST(ROWGUID AS VARCHAR(38)) AS ROWGUID,
        CAST(MODIFIEDDATE AS DATE) AS MODIFIEDDATE
    FROM 
        {{ ref('stg_adventure_works__product') }}
)

SELECT * FROM PRODUCT